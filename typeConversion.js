//implicit number conversion 
console.log(12 * "2");
console.log(12 * "a");
console.log(12*Infinity);
console.log(12* -Infinity);
console.log(12 * null);
console.log(12 * NaN);
console.log(12*undefined);
console.log(12* true+"\n");

//Conversion for addition
console.log(2 + Infinity);
console.log(2 + -Infinity);
console.log(2 + null);
console.log(2 + NaN);
console.log(2 + undefined);
console.log(2 + true+"\n");

//string conversion
console.log('2' + 12);
